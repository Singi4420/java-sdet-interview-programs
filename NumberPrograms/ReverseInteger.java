public class ReverseInteger{
	  
	  
	  public static void main(String[] args){
	  
	  int num = 7383;
	  int rev_num = 0;
	  
	  while(num >0){
	  
	  int num1 = num % 10;
	  rev_num = rev_num * 10 + num1;
	  num = num/10;
	  
	  
	  
	  
	  
	  }
	  
	  System.out.println("The Reverse integer is " + rev_num);
	  
	  
	  
	  
	  
	  
	  }
	  
	  
	  









}